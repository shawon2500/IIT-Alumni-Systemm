    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = DriverManager.getConnection(
                       "jdbc:mysql://localhost:3306/db", "root", "BokhtiarS250@");
            Statement st=con.createStatement();
            ResultSet rs;
            if(fname.isSelected())
            {
                radio="`First Name`";
            }
            if(batch.isSelected())
            {
                radio="`Batch`";
            }
            if(cwp.isSelected())
            {
                radio="`Current Working Place`";
            }
            if(skills.isSelected())
            {
                radio="`Skills`";
            }
            rs=st.executeQuery("SELECT `Email`,`First Name`, `Last Name`, `Phone No`, `Address`, `Batch`, `Roll`, `Hall`, `Date of Birth`, `Current Working Place`, `Gender`, `Religion`, `Blood Group`, `Skills` FROM `project` WHERE"+radio+ "LIKE" +"'%"+skey.getText()+"%'");
            DefaultTableModel tb=(DefaultTableModel)jTable1.getModel();
            while(rs.next())
            {    String email=rs.getString("Email");
                String Fname=rs.getString("First Name");
                String Lname=rs.getString("Last Name");
                String Phone=rs.getString("Phone No");
                String Address=rs.getString("Address");
                String Batch=rs.getString("Batch");
                String Roll=rs.getString("Roll");
                String Hall=rs.getString("Hall");
                String Dob=rs.getString("Date of Birth");
                String Cwp=rs.getString("Current Working Place");
                String Gender=rs.getString("Gender");
                String Religion=rs.getString("Religion");
                String Blood=rs.getString("Blood Group");
                String Skills=rs.getString("Skills");
                //                 byte[] img = rs.getBytes("Image");
                //                 ImageIcon image = new ImageIcon(img);
                //                Image im = image.getImage();
                //Image myImg = im.getScaledInstance(.getWidth(), .getHeight(), Image.SCALE_SMOOTH);
                // ImageIcon newImage = new ImageIcon(myImg);
                //               /.setIcon(newImage);
                // Resize The ImageIcon
                Object[] data={email,Fname,Lname,Phone,Address,Batch,Roll,Hall,Dob,Cwp,Gender,Religion,Blood,Skills};

                tb.addRow(data);

            }

            skey.setText("");

            con.close();

        } catch (SQLException ex) {
            Logger.getLogger(SearchStudent.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_searchActionPerformed

